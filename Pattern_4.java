package loop_practice;

public class Pattern_4 {

	public static void main(String[] args) {
		System.out.println("For loop with pattern");
		
		for(int i=1;i<=6;i++)
		{
			for (int j=0;j<i;j++)
			{
				System.out.print("* ");
			}
			    System.out.println("");
		}
		for(int i=6;i>=1;i--)
		 {
			for (int j=0;j<i;j++)
			{
				System.out.print("* ");
			}
			    System.out.println("");
	     }
		 }
		
}			
		
	


