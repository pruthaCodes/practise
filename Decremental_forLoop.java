package loop_practice;

public class Decremental_forLoop {

	public static void main(String[] args) {
		System.out.println("For loop with pattern");
		
		int i, j;
		
        for(i=1; i<6; i++)
        {
            for(j=0; j<i; j++)  
            {    
            	System.out.print("* ");
            }
            System.out.println();
            
        } 
    } 
		
}


	



